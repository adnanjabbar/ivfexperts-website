PGT-A vs PGT-M
</h2>

<h3 class="text-xl font-semibold mb-4">PGT-A (Preimplantation Genetic Testing for Aneuploidy)</h3>
<p class="text-gray-700 leading-relaxed mb-6">
PGT-A screens embryos for chromosomal abnormalities (e.g., trisomy 21) and is commonly considered in advanced maternal age, recurrent implantation failure or recurrent miscarriage.
</p>

<h3 class="text-xl font-semibold mb-4">PGT-M (Preimplantation Genetic Testing for Monogenic Disorders)</h3>
<p class="text-gray-700 leading-relaxed">
PGT-M is used when one or both partners carry a known inherited genetic condition. Embryos are tested for that specific mutation before transfer.
</p>

</div>
</section>


