    <script>
        document.addEventListener('DOMContentLoaded', () => {
            const slides = document.querySelectorAll('.hero-slide');
            let current = 0;
            const interval = 4500;

            function showSlide(index) {
                slides.forEach((slide, i) => {
                    if (i === index) {
                        slide.classList.remove('opacity-0', 'z-0', 'pointer-events-none');
                        slide.classList.add('opacity-100', 'z-10', 'pointer-events-auto');
                    } else {
                        slide.classList.remove('opacity-100', 'z-10', 'pointer-events-auto');
                        slide.classList.add('opacity-0', 'z-0', 'pointer-events-none');
                    }
                });
            }

            showSlide(current);

            const rotator = setInterval(() => {
                current = (current + 1) % slides.length;
                showSlide(current);
            }, interval);

            // Pause rotation on hover (optional but user-friendly)
            const leftColumn = document.querySelector('.flex-col.justify-between');
            leftColumn.addEventListener('mouseenter', () => clearInterval(rotator));
            leftColumn.addEventListener('mouseleave', () => {
                clearInterval(rotator);
                const newRotator = setInterval(() => {
                    current = (current + 1) % slides.length;
                    showSlide(current);
                }, interval);
            });
        });
    </script>
