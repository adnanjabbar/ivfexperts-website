                <p class="text-gray-700 leading-relaxed mb-4">
                    Testes do not produce enough (or any) sperm due to impaired spermatogenesis.
                </p>
                <ul class="space-y-2 text-gray-700">
                    <li>• Genetic causes (Klinefelter syndrome, Y-chromosome deletions)</li>
                    <li>• Hormonal issues (hypogonadism)</li>
                    <li>• Testicular failure (chemotherapy, radiation, mumps orchitis)</li>
                    <li>• Idiopathic (unknown cause ~60%)</li>
                </ul>
                <p class="text-gray-700 mt-4 font-medium">Micro-TESE finds usable sperm in 50–60% of cases.</p>
