<section class="section-lg bg-white border-t border-gray-100">
    <div class="max-w-5xl mx-auto px-6">
        <h2 class="text-3xl lg:text-4xl font-bold text-center mb-10 text-gray-900">
            What Is Diminished Ovarian Reserve?
        </h2>

        <p class="text-lg text-gray-700 leading-relaxed mb-8 max-w-4xl mx-auto">
            DOR means your ovaries have fewer eggs than expected for your age, making conception harder. It's not the same as menopause — ovulation can still occur, but egg quality/quantity is reduced. Diagnosis is based on AMH, AFC, FSH, and age. In Pakistan, delayed marriage (average age 23–28) and urban stress contribute to rising cases.
        </p>

        <div class="grid md:grid-cols-2 gap-8">
            <div class="bg-teal-50/40 rounded-2xl p-8 shadow-md border border-teal-100">
                <h3 class="text-2xl font-bold text-teal-800 mb-6">Common Causes</h3>
                <ul class="space-y-2 text-gray-700">
                    <li>• Age (biggest factor — reserve drops 50% by age 35)</li>
                    <li>• Genetics (family history, chromosomal issues)</li>
                    <li>• Medical treatments (chemotherapy, radiation)</li>
                    <li>• Autoimmune conditions (e.g. thyroid, endometriosis)</li>
                    <li>• Lifestyle (smoking reduces reserve by 30–50%)</li>
                    <li>• Environmental toxins (pesticides, pollution)</li>
                    <li>• Surgery (ovarian cyst removal, endometriosis)</li>
                    <li>• Idiopathic (unknown — 20–30%)</li>
                </ul>
            </div>

            <div class="bg-teal-50/40 rounded-2xl p-8 shadow-md border border-teal-100">
                <h3 class="text-2xl font-bold text-teal-800 mb-6">Symptoms & Signs</h3>
                <ul class="space-y-2 text-gray-700">
                    <li>• Shorter cycles (<26 days)</li>
                    <li>• Difficulty conceiving >6–12 months</li>
                    <li>• Low AMH (<1 ng/mL)</li>
                    <li>• Low AFC (<5–7 follicles)</li>
                    <li>• High FSH (>10 IU/L on day 3)</li>
                    <li>• Poor response to stimulation meds</li>
                </ul>
            </div>
        </div>
    </div>
